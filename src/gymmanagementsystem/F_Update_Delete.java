    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int checkMaKh = 0;
        String search_MaKH = TF_MaKH.getText(); 
        try {
            // TODO add your handling code here:
            Connection con = Connect_data.mysql_Connection(); 
            Statement stmt = con.createStatement();
            ResultSet res=stmt.executeQuery("select * from new_member WHERE MaKH="+search_MaKH);
            
            while(res.next()) {
                checkMaKh = 1;
                TF_name.setText(res.getString(2));
                TF_DiaChi.setText(res.getString(3));
                TF_SDT.setText(res.getString(4)); 
                TF_Email.setText(res.getString(5));
                CB_GioiTinh.setSelectedItem(new String(res.getString(6)));
                TF_AGE.setText(res.getString(7));
                CB_TIME.setSelectedItem(new String(res.getString(8)));
//                CB_GoiDK.setSelectedItem(new String(res.getString(8)));
//                TF_Payment.setText(res.getString(9));
                NTGia.setText(res.getString(9));

            } 
            if(checkMaKh == 0){
                JOptionPane.showMessageDialog(null,"Mã khách hàng không tồn tại");
            }
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed
