//        String[] arrInput = Input.split("\\s+");
        if(validateFields())  {
                String Makh= TF_MaKH.getText();
                String TenKH= TF_name.getText();
                String DiaChi= TF_DiaChi.getText();
                String SDT= TF_SDT.getText();
                String email= TF_Email.getText();
                String Gioitinh=(String) CB_GioiTinh.getSelectedItem();
                String age= TF_AGE.getText();
                String TG_Tap= (String) CB_TIME.getSelectedItem();
//                String goidk= (String) CB_GoiDK.getSelectedItem();
//                String tongtien = arrInput[0];
                String ngaytg = NTGia.getText();
                Connection con = Connect_data.mysql_Connection(); 
               try {
                String query = "update new_member set TenKH=?, DiaChi=?, SDT=?, email=?, gioitinh=?, tuoi=?, TG_Tap=?, NgayTG=? Where MaKH = ?";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1,TenKH);
                ps.setString(2,DiaChi);
                ps.setString(3,SDT);
                ps.setString(4,email);
                ps.setString(5,Gioitinh);
                ps.setString(6,age);
                ps.setString(7,TG_Tap);
//                ps.setString(7,goidk);
//                ps.setString(8,tongtien);
                ps.setString(8,ngaytg);
                ps.setString(9,Makh);
                
                ps.executeUpdate(); 
                
                JOptionPane.showMessageDialog(null, "Update thành công "); 
                this.setVisible(false);  
                new F_Update_Delete().setVisible(true); 
               
            }catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e); 
            }
        } 
